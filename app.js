let trafficColor = prompt("Enter a Traffic Light Color (Should Be Starting With Capital Letter)")
if(trafficColor == "Red")
    {document.write("Red Says STOP")}
else if (trafficColor == "Yellow")
    {document.write("Yellow Says READY TO MOVE")}
else if (trafficColor == "Green")
    {document.write("Green Says GO")}
else{document.write("Please Enter Only A Traffic Light Color")}